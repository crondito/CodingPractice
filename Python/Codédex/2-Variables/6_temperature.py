# Operators

score = 0           # score is 0
score = 4 + 3       # score is now 7
score = 4 - 3       # score is now 1
score = 4 * 3       # score is now 12
score = 4 / 3       # score is now 1.3333
score = 4 % 3       # score is now 1

print(score)        # Output: 1

# Calculate 20% tip

pizza = 2.99
coke = 0.99

tip = (pizza + coke) * 0.2

print(tip)  # Output: 0.796

# Temperature

temp_in_f = 52

temp_in_c = (temp_in_f - 32) / 1.8

print(temp_in_c)  # Output: 11.11
